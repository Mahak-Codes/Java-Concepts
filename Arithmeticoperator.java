package BasicsOfJava;

public class Arithmeticoperator {
    public static void main(String[] args) {
        int x = 5, y = 2; //int datatype variable
        double a = 5, b = 2;//double datatype
        System.out.println("Operators on int datatype:");//Operators on int datatype
        System.out.println("Addition:" + (x + y));
        System.out.println("Subtraction:" + (x - y));
        System.out.println("Multiplication:" + (x * y));
        System.out.println("Division:" + (x / y));
        System.out.println("Modulo:" + (x % y));
        System.out.println("Operators on double datatype :");
        System.out.println("Addition:" + (a + b));
        System.out.println("Subtraction:" + (a - b));
        System.out.println("Multiplication:" + (a * b));
        System.out.println("Division:" + (a / b));
        System.out.println("Modulo:" + (a % b));
        System.out.println("Division Operator :");
        double z = 5 / 2;
        System.out.println(5 / 2);//2
        System.out.println(z); //2.0
        System.out.println(a / b);//2.5;
        System.out.println(5 / 2.0);//2.5
        System.out.println(5.0 / 2);//2.5
        System.out.println(5.0 / 2.0);//2.5;
    }
}
